/** Returns unique, deduplicated search suggestion strings from the dataset */
export function getSearchSuggestions(query) {
  if (!query || query.trim().length < 1) return [];
  const q = query.toLowerCase().trim();

  const suggestions = new Set();

  pgData.forEach(pg => {
    if (pg.name.toLowerCase().includes(q)) suggestions.add(pg.name);
    if (pg.location.toLowerCase().includes(q)) suggestions.add(pg.location);
    if (pg.locationDetail && pg.locationDetail.toLowerCase().includes(q)) suggestions.add(pg.locationDetail);
  });

  // Also add location tokens like "AECS Layout", "Green Glen Layout", etc.
  pgData.forEach(pg => {
    const parts = (pg.locationDetail || '').split(/[,-]/);
    parts.forEach(part => {
      const trimmed = part.trim();
      if (trimmed.toLowerCase().includes(q) && trimmed.length > 2) {
        suggestions.add(trimmed);
      }
    });
  });

  return Array.from(suggestions).slice(0, 8);
}
